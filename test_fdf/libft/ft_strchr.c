#include "libft.h"

char				*ft_strchr(const char *s, int c)
{
	while (*s != '\0')
	{
		if (*s == (char)c)
			return ((char*)s);
		s++;
	}
	if (*s == '\0' && *s != (char)c)
		return (NULL);
	else
		return ((char*)s);
}
