#include "libft.h"

char				*ft_strcat(char *s1, const char *s2)
{
	int				i;
	int				p;

	i = 0;
	p = ft_strlen(s1);
	while (s2[i])
	{
		s1[p] = s2[i];
		i++;
		p++;
	}
	s1[p] = '\0';
	return (s1);
}
